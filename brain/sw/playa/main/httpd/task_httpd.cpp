void task_httpd_create() {

    static uint8_t vParameters = 0;
    TaskHandle_t tHandle = NULL;

    BaseType_t tcResult;

    ESP_LOGI(TAG, "Starting httpds task...");
    // vTaskDelay(5 * xPortGetTickRateHz());

    tcResult  = xTaskCreate(task_httpd_main, "httpd", TASK_HTTPD_STACK_SIZE,
                            &vParameters, TASK_HTTPD_PRIORITY, &tHandle);

    if (tcResult != pdPASS) {
        ESP_LOGE(TAG, "Failed to create httpd task = %d", tcResult);
    } else {
        ESP_LOGI(TAG, "Httpd task started");
    }

}
